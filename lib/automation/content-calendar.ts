/**
 * Check if today falls within a seasonal topic range.
 * Returns the seasonal topic if found, null otherwise.
 */
export function getSeasonalTopic(): SeasonalTopic | null {
  const now = new Date();
  const month = now.getMonth() + 1; // 1-12
  const day = now.getDate();

  for (const topic of SEASONAL_TOPICS) {
    if (topic.startMonth === topic.endMonth) {
      // Same month range
      if (month === topic.startMonth && day >= topic.startDay && day <= topic.endDay) {
        return topic;
      }
    } else {
      // Cross-month range
      if (
        (month === topic.startMonth && day >= topic.startDay) ||
        (month === topic.endMonth && day <= topic.endDay) ||
        (month > topic.startMonth && month < topic.endMonth)
      ) {
        return topic;
      }
    }
  }

  return null;
}

/**
 * Get today's topic index using day-of-year rotation.
 * Deterministic — same date always gives same topic.
 */
export function todayIndex(poolLength: number): number {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  return dayOfYear % poolLength;
}

/**
 * Get today's current affairs angle (seasonal override or regular rotation)
 */
export function getTodayCurrentAffairsAngle(): string {
  const seasonal = getSeasonalTopic();
  if (seasonal) return seasonal.currentAffairs;
  return CURRENT_AFFAIRS_ANGLES[todayIndex(CURRENT_AFFAIRS_ANGLES.length)];
}

